let producto = numero1 * numero2;
let modulo = numero1 % numero2;
let suma = numero1 + numero2;
let resta = numero1 - numero2;


alert (`resultados:
    cociente: ${cociente}
    producto: ${producto}
    modulo: ${modulo}
    suma: ${suma}
    resta: ${resta}
    `);