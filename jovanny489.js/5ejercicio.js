alert ("Quieres saber el radio de un circulo");
let radio = parseFloat(prompt("ingrese el radio de su circulo"));
const pi = 1.1415
let area = (pi * radio)
let perimetro = (2 * pi * radio)
alert (`el radio de su triangulo es de ${radio} 
    el area es de ${area}
    el perimetro es de ${perimetro}`)